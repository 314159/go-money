package currency

import "github.com/314159/go-money/money"

type currency struct {
	alphaCode     string
	numericCode   string
	decimalDigits int
}

func (c currency) AlphaCode() string {
	return c.alphaCode
}

func (c currency) NumericCode() string {
	return c.numericCode
}

func (c currency) DecimalDigits() int {
	return c.decimalDigits
}

func (c currency) String() string {
	return c.AlphaCode()
}

// New constructs a currency object
func New(alphaCode, numericCode string, decimalDigits int) *currency {
	c := currency{
		alphaCode:     alphaCode,
		numericCode:   numericCode,
		decimalDigits: decimalDigits,
	}
	return &c
}

